package hiCampBean;

public class ForumAlbumBean implements java.io.Serializable{
	private static final long serialVersionUID = 1L;
	private String forumAlbumNo;
	private String forumNo;
	private String forumAlbum;
	
	public String getForumAlbumNo() {
		return forumAlbumNo;
	}
	public void setForumAlbumNo(String forumAlbumNo) {
		this.forumAlbumNo = forumAlbumNo;
	}
	public String getForumNo() {
		return forumNo;
	}
	public void setForumNo(String forumNo) {
		this.forumNo = forumNo;
	}
	public String getForumAlbum() {
		return forumAlbum;
	}
	public void setForumAlbum(String forumAlbum) {
		this.forumAlbum = forumAlbum;
	}
	
	
}
